public class Loops {
    public static void main(String[] args){

        System.out.println("Using for loop");
        for(int i = 1; i <= 100; i++){
            System.out.println(i);
        }

        System.out.println("Using while loop");
        int i = 1;
        while(i < 101){
            System.out.println(i);
            i++;
        }
    }
}
